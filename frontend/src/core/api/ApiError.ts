export type Problem = {
  type: string;
  title: string;
  status: number;
  detail: string;
  instance?: string;
};

export type BackendErrorType = {
  problem: Problem;
  code: BackendError;
  type?: string;
};

export enum BackendError {
  _InternalError = -2,
  _UnknownError = -1,
  BackendError = 100,

  UserNotVerified = 110,
  MissingCookie = 111,
  UserNotFound = 112,
  PasswordDoNotMatch = 113,
  SessionIdNotMatching = 114,
  InvalidEmail = 115,
  InvalidUsername = 116,
  InvalidPassword = 117,

  EmailAlreadyTaken = 120,
  UsernameAlreadyTaken = 121,
  AccountFailedToVerify = 122,

  DeletingUserFailed = 135,

  GameNotFound = 140,
  GameNameMissing = 141,
  JoinFailed = 142,
  OpcodeMissing = 143,
  InvalidOpcode = 144,
  InvalidGameId = 145,
  AlreadyPlaying = 146,
  GameIsFull = 147
}

